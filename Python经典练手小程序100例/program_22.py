"""
【程序22】
题目： 两个乒乓球队进行比赛，各出三人。甲队为a,b,c三人，乙队为x,y,z三人。已抽签决定比赛名单。有人向队员打听比赛的名单。a说他不和x比，c说他不和x,z比，请编程序找出三队赛手的名单。
程序分析： 假定甲队a,b,c的顺序已定，先生成乙队可能的出场顺序，判断是否满足要求，若满足，则输出结果。
"""

A = ['a', 'b', 'c']
B = ['x', 'y', 'z']

for i in B:
    for j in B:
        if j != i:
            for k in B:
                if k != i and k != j:
                    if (i != 'x') and (k != 'x') and (k != 'z'):
                        print('%s--%s,%s--%s,%s--%s' % (A[0], i, A[1], j, A[2], k))
